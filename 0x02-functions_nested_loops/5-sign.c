#include "main.h"
/**
 *print_sign - print the sign of a number
 *@n: random number
 *Return: 1 if positive, 0 if it is zero and -1 otherwise
 **/

int print_sign(int n);

{
if (n > 0)
{
_putchar("+, n");
return (1);
}
else if(n = 0)
{
_putchar("0, n");
return (0);
}
else
{
_putchar("-, n");
return (-1);
}
}
