/**
 *op_add - addition
 *@a: number given
 *@b: number given
 *Return: operation
 **/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - difference
 *@a: number given
 *@b: number given
 *Return: operation
 **/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - product
 *@a: number given
 *@b: number given
 *Return: operation
 **/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - division
 *@a: number given
 *@b: number given
 *Return: operation
 **/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - module
 *@a: number given
 *@b: number given
 *Return: operation
 **/
int op_mod(int a, int b)
{
	return (a % b);
}
